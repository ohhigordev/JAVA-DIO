package edu.java.Arrays;

public class Arrays1 {
    public static void main(String[] args) {
        // Sintaxe para criar um array:
        int [] idade  = new int [3]; // 0 , 1 , 2. (3 espaços na memória)
        idade [0] = 20;
        idade [1] = 15;
        idade [2] = 30;

        // Imprimindo os valores armazenados:
        System.out.println("Idade 01: " + idade[0]);
        System.out.println("Idade 02: " + idade[1]);
        System.out.println("Idade 03: " + idade[2]);
        
    }
}
