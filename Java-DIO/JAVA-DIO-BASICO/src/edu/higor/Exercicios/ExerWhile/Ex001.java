package edu.higor.Exercicios.ExerWhile;


public class Ex001 {
    public static void main(String[] args) {
        /*Contagem crescente: Escreva um programa que utilize um laço while para imprimir os números de 1 a 10. */
        int contagem = 0;

        while(contagem <= 10) {
            System.out.println(contagem);
            contagem++;
        }
        
    }
}
